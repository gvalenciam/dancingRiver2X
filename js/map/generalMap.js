function polygonCenter(poly) {

    var lowx,
        highx,
        lowy,
        highy,
        lats = [],
        lngs = [],
        vertices = poly.getPath();

    for(var i = 0; i<vertices.length; i++) {
        lngs.push(vertices.getAt(i).lng());
        lats.push(vertices.getAt(i).lat());
    }

    lats.sort();
    lngs.sort();
    lowx = lats[0];
    highx = lats[vertices.length - 1];
    lowy = lngs[0];
    highy = lngs[vertices.length - 1];
    center_x = lowx + ((highx-lowx) / 2);
    center_y = lowy + ((highy - lowy) / 2);
    return (new google.maps.LatLng(center_x, center_y));

}

function polygonCenterTotal() {

    var sumLat = 0;
    var sumLng = 0;

    for (var t = 0; t < centroids.length; t++){

        sumLat += centroids[t].lat();
        sumLng += centroids[t].lng();

    }

    return {centerX: sumLat/centroids.length, centerY: sumLng/centroids.length}

}

function coordinatesArrayCenter(coordinatesArray) {

    var sumLat = 0;
    var sumLng = 0;

    for (var t = 0; t < coordinatesArray.length; t++){

        sumLat += coordinatesArray[t].lat();
        sumLng += coordinatesArray[t].lng();

    }

    return {centerX: sumLat/coordinatesArray.length, centerY: sumLng/coordinatesArray.length}

}

function getRiverJSONData(filePath, subDataArray, dataArray, JSONRiver, propertiesArray){

    subDataArray = [];

    $.getJSON(filePath, function (json) {

        for(var i = 0; i < json.features.length; i++){

            for (var j = 0; j < json.features[i].geometry.coordinates[0].length; j++){

                var subcoords = {lat: json.features[i].geometry.coordinates[0][j][1], lng: json.features[i].geometry.coordinates[0][j][0]};
                subDataArray.push(subcoords);

            }

            propertiesArray.push(json.features[i]["properties"]);
            dataArray.push(subDataArray);
            subDataArray = [];

        }

    }).done(function () {

        dataCounter++;

        if(dataCounter === JSONRiver.length){

            addPolygonForRiver(JSONRiver[0].dataArray, JSONRiver[0].propertiesArray);
            var polygonBounds = polygonCenterTotal();
            //map.setCenter(new google.maps.LatLng(polygonBounds.centerX, polygonBounds.centerY));
            map.setZoom(8);
            map.panTo(new google.maps.LatLng(polygonBounds.centerX, polygonBounds.centerY));
            dataCounter = 0;

        }

    });

}

